package com.elogiclab.cns;

public class CnsCardException extends Exception {

	public CnsCardException() {
		super();
	}

	public CnsCardException(String message, Throwable cause) {
		super(message, cause);
	}

	public CnsCardException(String message) {
		super(message);
	}

	public CnsCardException(Throwable cause) {
		super(cause);
	}

}
